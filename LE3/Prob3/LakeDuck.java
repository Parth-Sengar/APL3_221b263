public class LakeDuck implements Swimmable, Flyable, Quackable {
    public void swim() {
        System.out.println("LakeDuck swims.");
    }

    public void fly() {
        System.out.println("LakeDuck flies.");
    }

    public void quack() {
        System.out.println("LakeDuck quacks.");
    }
}
