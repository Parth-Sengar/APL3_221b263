public class RedHeadDuck implements Swimmable, Flyable, Quackable {
    public void swim() {
        System.out.println("RedHeadDuck swims.");
    }

    public void fly() {
        System.out.println("RedHeadDuck flies.");
    }

    public void quack() {
        System.out.println("RedHeadDuck quacks.");
    }
}
